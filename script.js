  		//mouseover 오브젝트 애니메이션
  		//KOR
  		$("#kor").on({
            mouseover: (e) => {
                $("#pinkkor").show();
                $("#pinkkor").css({
                      opacity: 1,
                      position:"absolute"
                  })
            	},
            mouseout: (e) => {
                $("#pinkkor").hide();
                $("#pinkkor").css({
                      opacity: 0,
                      position:"absolute"
                  })
            	}
        });

